  <h3><b>Quem somos?</b></h3>
  <p>
O projecto wirelesspt.net é uma rede wireless mesh em que todos e quaisquer routers e ou dispositivos de roteamento que sejam adicionados à rede comunicam entre si à semelhança de como funciona a internet.  Com este tipo de conceito qualquer um de nós cria assim uma rede idêntica à internet no momento em que activamos mais um router.</p>

<p>Além do baixo custo de implementação e desenvolvimento, uma outra vantagem deste tipo de rede é que não pode ser completamente desligada porque ninguém controla todos os routers e não existe ponto de controlo central.</p>

<p>O router ou dispositivo de roteamento utilizado é exactamente o mesmo tipo de router podemos adquirir em qualquer loja de material informático e no qual se instala e utiliza mvwrt que é um software/firmware específico desenvolvido pelo projecto wirelesspt.net. Este software fará com que o router detecte a restante rede mesh e se encaminhe automaticamente a outros routers da zona circundante, inserindo-se desta forma na rede que o próprio ajuda a criar e a expandir.</p>

<p>Quantos mais routers, maior será a rede e sua cobertura. Alguns destes routers da rede poderão ou estarão ligados à internet conforme se deseje e desta forma todos os utilizadores da wirelesspt.net terão também acesso à internet.
  </p>
