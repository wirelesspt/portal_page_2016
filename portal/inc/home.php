  <h3><b>Como funciona?</b></h3>
  <p>
A nossa <a href="http://wirelesspt.net/wiki/Objectivos_wirelesspt" target="_blank">visão e missão </a> consiste no desenvolvimento de redes wireless mesh comunitárias por nós mesmos e para todos nós. Estas redes são independentes de infraestrutura, empresa, e ou entidade centralizada e tem como  objectivo fomentar o acesso democrático, livre e gratuito ás tecnologias de informação dos seus utilizadores em qualquer situação através da partilha de recursos tecnológicos da comunidade para a mesma ajudando indivíduos, organizações e projectos na implementação de redes wireless para fins de beneficio livre do cidadão.</p>

<p>Apostamos ainda no desenvolvimento e investigação de tecnologias informáticas, digitais, telecomunicações, de código aberto e livre assim como promover, educar e fornecer informação tecnológica de forma a cultivar o meio social sobre a importância de segurança e privacidade do utilizador online.
  </p>
