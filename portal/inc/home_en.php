  <h3><b>How does it work?</b></h3>
<p>Wirelesspt.net is a mesh network development project that is not dependent of central infrastructure, corporation or entity which is done by the ordinary citizen with the aim to <a href="http://wirelesspt.net/wiki/Objectivos_wirelesspt" target="_blank">provide free, open and democratic access</a> to the highways of information technologies for it's users at any given time or situation through the share of technological resources within it's community, helping people and organizations implementing wireless networks that will benefit their communities.</p>
<p>The project also invests in investigating free, open source, digital, information and telecommunication technologies as well as to promote, educate and supply technological information to teach and educate it's surrounding social environment about the importance of online privacy and security.</p>
