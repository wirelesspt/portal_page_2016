  <h3><b>Who are we?</b></h3>
<p>Wirelesspt.net is a mesh network project in which all routers and or routing devices that are added to it, will automatically communicate between themselves in a similar fashion how the Internet works, creating this way and identical system like the Internet but without cables or wires. This kind of network concept allows us to create a network and expand it every time we activate another router nearby.</p>
<p>Aside it's very low cost of development and implementation, another advantage of this type of network is that it cannot be completely shutdown since no one controls all routers or access points and there is no central control point.</p>
<p>The router or routing device used is exactly the same type of router that we can obtain in any regular computer store in which we will a very specific type of software/firmware developed by the wirelesspt.net and known as mvwrt. This software will make the router automatically detect the rest of the nearby mesh network, add itself to it while simultaneously help expanding it.</p>
<p>The more routers the network has the wider will be it's coverage. Some of these routers can be or will be connected to the Internet as desired and this way all wirelesspt.net users will also have Internet access.</p>
